namespace OBeautifulCode.Excel
{
    /// <summary>
    /// Specifies the orientation of images relative to each other.
    /// </summary>
    public enum ImagesRelativeOrientation
    {
        /// <summary>
        /// Unknown (default).
        /// </summary>
        Unknown,

        /// <summary>
        /// Orient images horizontally relative to each other (left-to-right).
        /// </summary>
        Horizontal,

        /// <summary>
        /// Orient images vertically relative to each other (top-to-bottom).
        /// </summary>
        Vertical,
    }
}
